public class cast {
    public static void main(String[] args) {
        // casting (implicit)
        double price = 100.00;
        double finalPrice = price + 10;
        System.out.println(finalPrice);
        // explicit casting
        int p = 100;
        int fP = p + (int)18.18;
        System.out.println(fP);


    }
}
